RegisterCommand('gps', function(source, args)
    if #args ~= 1 then
        ESX.ShowNotification('Hai sbagliato a scrivere il comando.')
        return
    end

    if args[1] == nil or tonumber(args[1]) == nil then
        ESX.ShowNotification('Devi scrivere un civico della mappa per impostare il GPS!')
        return
    end

    for k,v in ipairs(Civici) do
        if k == args[1] then
            ESX.ShowNotification('Hai impostato il GPS.')
            SetNewWaypoint(v)
            return
        end
    end

    ESX.ShowNotification('Questo civico non esiste!')
end, false)